import cv2
import numpy as np

image = cv2.imread('input.jpg')

if image is None:
    print("Помилка: Не вдалося завантажити зображення. Будь ласка, перевірте шлях до файлу.")
    exit()

# ---------------------------------------------------------
# 2. РОЗМИТТЯ (BLURRING) ЗОБРАЖЕННЯ
# ---------------------------------------------------------
# Метод А: Використання вбудованої функції OpenCV (Гаусове розмиття)
# (15, 15) - це розмір ядра (kernel). Більші числа = сильніше розмиття.
blurred_image_gaussian = cv2.GaussianBlur(image, (15, 15), 0)

# Метод Б: Використання 2D згортки з власним ядром усереднення
# Створюється матриця 5x5, заповнена одиницями, яка ділиться на 25 для нормалізації.
kernel_blur = np.ones((5, 5), np.float32) / 25
blurred_image_conv = cv2.filter2D(image, -1, kernel_blur)

# ---------------------------------------------------------
# 3. ПІДВИЩЕННЯ РІЗКОСТІ (SHARPENING) ЗОБРАЖЕННЯ
# ---------------------------------------------------------
# Створення власного ядра для підвищення різкості.
# Центральний піксель має велику вагу (9), а сусідні пікселі віднімаються (-1).
kernel_sharpening = np.array([[-1, -1, -1],
                              [-1,  9, -1],
                              [-1, -1, -1]])

# Застосування ядра різкості за допомогою 2D згортки
sharpened_image = cv2.filter2D(image, -1, kernel_sharpening)

# ---------------------------------------------------------
# 4. ВІДОБРАЖЕННЯ РЕЗУЛЬТАТІВ
# ---------------------------------------------------------
# Відображення оригінального та змінених зображень в окремих вікнах
cv2.imshow('1. Original Image', image)
cv2.imshow('2. Blurred (Gaussian)', blurred_image_gaussian)
cv2.imshow('3. Blurred (Convolution)', blurred_image_conv)
cv2.imshow('4. Sharpened', sharpened_image)

# Нескінченне очікування натискання будь-якої клавіші, після чого всі вікна безпечно закриваються
cv2.waitKey(0)
cv2.destroyAllWindows()